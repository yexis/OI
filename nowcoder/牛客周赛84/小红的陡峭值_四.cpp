void solve() {
    int n;
    cin >> n;
    
    ll tot = 0;
    vector<int> g[n];
    for (int i = 0; i < n - 1; i++) {
        int u, v;
        cin >> u >> v;
        u--, v--;
        g[u].push_back(v);
        g[v].push_back(u);
        tot += abs(u - v);
    }
    // cout << tot << endl;

    ll ans = LLINF;
    auto dfs1 = [&](auto&& self, int u, int o) -> ll {
        ll v1 = 0;
        for (auto& v : g[u]) {
            if (v == o) {
                continue;
            }
            ll t = abs(u - v);
            ll v2 = self(self, v, u);
            ans = min(ans, abs(tot - v2 - t - v2));

            v1 += v2 + t;
        }
        return v1;
    };

    dfs1(dfs1, 0, -1);
    cout << ans << "\n";
}

