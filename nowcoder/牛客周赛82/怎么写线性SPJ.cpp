void solve() {
    int n;
    cin >> n;
    if (n == 1) {
        cout << 1 << "\n";
        cout << 1 << "\n";
        return;
    }
    if (n == 2) {
        cout << 2 << "\n";
        cout << 1 << " " << 2 << "\n";
        return;
    }

    // 第一个小于等于n的2次幂数
    int gn = 31 - __builtin_clz(n);
    int ans = gn + 1;
    cout << ans << "\n";
    
    vector<vector<int> > v(20);
    for (int d = 1; d < 20; d++) {
        if ((1 << d) > 2000) break;
        v[d] = v[d - 1];
        for (int i = v[d - 1].size() - 2; i >= 0; i--) {
            v[d].push_back(v[d - 1][i]);
        }
        v[d].push_back(d);
    }
    
    auto res = v[gn + 1];
    int diff = n - (1 << gn);
    int j = v[gn + 1].size() - 2;
    while (diff > 0 && j >= 0) {
        res.push_back(v[gn + 1][j]);
        diff--;
        j--;
    }
    for (auto& e : res) cout << e << " "; cout << "\n";
    
    return;
}

