void solve() {
    int n;
    cin >> n;
    vector<int> dp(n + 1);
    dp[1] = 1;
    dp[2] = 2;
    for (int i = 3; i <= n; i++) {
        dp[i] = (dp[i - 1] + dp[i - 2]) % mod;
    }
    ll ans = 1;
    for (int i = 2; i <= n; i++) {
        ans *= dp[i];
        ans %= mod;
    }
    cout << ans << "\n";
}

