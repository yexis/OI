void solve() {
    int n, m, q;
    cin >> n >> m >> q;

    vector<int> all(n + 1);
    vector<unordered_set<int>> view(n + 1);
    while (q--) {
        int op; cin >> op;
        if (op == 1) {
            int x, y;
            cin >> x >> y;
            view[x].insert(y);
        } else if (op == 2) {
            int x; cin >> x;
            all[x] = 1;
        } else if (op == 3) {
            int x, y; cin >> x >> y;
            if (all[x] || view[x].count(y)) {
                cout << "Yes" << "\n";
            } else {
                cout << "No" << "\n";
            }
        }
    }
}

